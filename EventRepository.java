package com.tushar.ticket.repositories; 
import java.util.Optional;
import java.util.UUID;


import org.springframework.data.domain.Page;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import com.tushar.ticket.domain.entities.Event;
import com.tushar.ticket.domain.entities.EventStatusEnum;

@Repository
public interface EventRepository extends JpaRepository<Event, UUID> {

    Page<Event> findByOrganizerId(UUID organizerId,org.springframework.data.domain.Pageable pageable);
    Optional<Event> findByIdAndOrganizerId(UUID id,UUID organizerId);
    Page<Event>findByStatus(EventStatusEnum status,org.springframework.data.domain.Pageable pageable);
    
}
